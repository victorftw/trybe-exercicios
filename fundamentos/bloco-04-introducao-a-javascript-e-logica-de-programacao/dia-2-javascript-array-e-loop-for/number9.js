//Utilizando o array criado no exercício anterior imprima o resultado da divisão de cada um dos elementos por 2.

// 1 - Criar um array vazio;
// 2 - Criar um loop que percorra do numero 1 até 25;
// 3 - Adicionar cada número obtido pelo loop dentro do array com array.push();
// 4 - Criar outro loop que percorre o array;
// 5 - Dar um console.log() fazendo a divisão de cada elemento dentro do array por 2;

let array = [];

for (let index = 1; index <= 25; index += 1) {
  array.push(index);
}

for (number of array) {
  console.log('A divisão de', number + ' por 2 é', number / 2);
}
